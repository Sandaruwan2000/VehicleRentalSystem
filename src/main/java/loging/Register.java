package loging;

public class Register {
	
	
	
	int id ;
	String name ;
	String email ;
	String password ;
	String mobile ;
	
	
	
	
	public Register(int id,String name, String email, String password, String mobile) {
		
		this.id=id ;
		this.name = name;
		this.email = email;
		this.password = password;
		this.mobile = mobile;
		
	}


	public int getId() {
		return id;
	}
	

	public String getName() {
		return name;
	}



	



	public String getEmail() {
		return email;
	}




	public String getPassword() {
		return password;
	}




	public String getMobile() {
		return mobile;
	}



	





	
	
	
	

}
